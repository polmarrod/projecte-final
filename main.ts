function jumpAnimation() {
    animation.stopAnimation(animation.AnimationTypes.All, mario)
    if (mario.vx > 0) {
        mario.setImage(assets.image`
            jump_right
        `)
    } else if (mario.vx < 0) {
        mario.setImage(assets.image`
            jump_left
        `)
    }
    
}

sprites.onOverlap(SpriteKind.Player, SpriteKind.Shroom, function on_on_overlap(sprite: Sprite, otherSprite2: Sprite) {
    if (sprite.y < otherSprite2.top) {
        otherSprite2.vx = 0
        animation.stopAnimation(animation.AnimationTypes.All, otherSprite2)
        jump()
        otherSprite2.setImage(assets.image`
            shroom_death
        `)
        pause(450)
        sprites.destroy(otherSprite2)
        info.changeScoreBy(100)
    } else {
        deathMario()
    }
    
})
